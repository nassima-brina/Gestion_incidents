   private void loadByPeriode(Date dateDebut, Date dateFin) {
    model.setRowCount(0);

    List<Incident> list = dao.findByPeriode(dateDebut, dateFin);

    for (Incident i : list) {
        model.addRow(new Object[]{
            i.getIdIncident(),
            i.getPriorite(),
            i.getStatut(),
            i.getEquipement().getIdEquipement(),
            i.getTechnicien().getIdTechnicien()
        });
    }
}

