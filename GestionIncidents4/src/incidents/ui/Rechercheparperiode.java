    private void RechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RechercheActionPerformed

    Date dateDebut = dateOuvertureChooser.getDate();
    Date dateFin = dateClotureChooser.getDate();

    // ✅ Vérifier si dates sélectionnées
    if (dateDebut == null || dateFin == null) {
        JOptionPane.showMessageDialog(this,
                "Veuillez sélectionner les deux dates !",
                "Erreur",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    // ✅ Vérifier période valide
    if (dateDebut.after(dateFin)) {
        JOptionPane.showMessageDialog(this,
                "La date de début doit être avant la date de fin !",
                "Erreur",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    // ✅ Appel DAO (utilise celui déjà déclaré en haut)
    List<Incident> list = dao.findByPeriode(dateDebut, dateFin);

    model.setRowCount(0);

    // ✅ Si aucun résultat
    if (list == null || list.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Aucun incident trouvé dans cette période !",
                "Information",
                JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    // ✅ Affichage résultats
    for (Incident i : list) {
        model.addRow(new Object[]{
            i.getIdIncident(),
            i.getPriorite(),
            i.getStatut(),
            i.getEquipement().getNom(),
            i.getTechnicien().getNom()
        });
    }

        
    }//GEN-LAST:event_RechercheActionPerformed


